    private void Add_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Add_buttonActionPerformed
        // TODO add your handling code here:
        if (name_warning_label.isVisible() || phone_warning_label.isVisible() || check_in_date_warning_label.isVisible() || check_out_date_warning_label.isVisible() || nights_warning_label.isVisible() || rooms_warning_label.isVisible()) {
            JOptionPane.showMessageDialog(null, "Please Input all of information !", "WARNING", JOptionPane.WARNING_MESSAGE);
        } else {
            String name = Name_text.getText();
            String phone = Phone_text.getText();
            String rooms = Rooms_jComboBox.getSelectedItem().toString();
            String night = Nights_jComboBox.getSelectedItem().toString();
            String check_in_date = check_in_date_jComboBox.getSelectedItem().toString();
            String check_out_date = check_out_date_jComboBox.getSelectedItem().toString();
            String hotel = "Unknown";

            Enumeration<AbstractButton> buttons = Hotels_buttonGroup.getElements();
            while (buttons.hasMoreElements()) {
                AbstractButton nextElement = buttons.nextElement();
                if (nextElement.isSelected()) {
                    hotel = nextElement.getText();
                    break;
                }
            }
            myPerson p = new myPerson(name, phone, night, rooms, check_in_date, check_out_date, hotel);
            if ((!name_warning_label.isVisible()) && (!phone_warning_label.isVisible()) && (!nights_warning_label.isVisible()) && (!rooms_warning_label.isVisible()) && (!check_in_date_warning_label.isVisible()) && (!check_out_date_warning_label.isVisible())) {
                myListModel.addElement(p);
                jList1.setModel(myListModel);
                Name_text.setText("");
                Phone_text.setText("");
                Nights_jComboBox.setSelectedIndex(0);
                Rooms_jComboBox.setSelectedIndex(0);
                check_in_date_jComboBox.setSelectedIndex(0);
                check_out_date_jComboBox.setSelectedIndex(0);
                name_warning_label.setVisible(true);
                phone_warning_label.setVisible(true);
                JOptionPane.showMessageDialog(null,
                        "Reserve Successfully !", "Success", JOptionPane.PLAIN_MESSAGE);
            }
        }

    }//GEN-LAST:event_Add_buttonActionPerformed

