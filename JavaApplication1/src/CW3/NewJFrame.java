    private void check_in_date_jComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_check_in_date_jComboBoxActionPerformed
        // TODO add your handling code here:
        if (check_in_date_jComboBox.getSelectedIndex() != 0) {
            check_in_date_warning_label.setVisible(false);
            if (check_out_date_jComboBox.getSelectedIndex() != 0) {
                DateTimeFormatter df = DateTimeFormatter.ofPattern("yyyy-MM-dd");
                LocalDate check_out_date = LocalDate.parse(check_out_date_jComboBox.getSelectedItem().toString(), df);
                LocalDate check_in_date = LocalDate.parse(check_in_date_jComboBox.getSelectedItem().toString(), df);
                Period next = Period.between(check_in_date, check_out_date);
                int days = next.getDays();
                if (days <= 0) {
                    JOptionPane.showMessageDialog(null, "Check Out date should behind Check In date !", "WARNING", JOptionPane.WARNING_MESSAGE);
                    check_out_date_jComboBox.setSelectedIndex(0);
                } else {
                    Nights_jComboBox.setSelectedItem(String.valueOf(days));
                }
            }

        } else {
            check_in_date_warning_label.setVisible(true);
        }
    }//GEN-LAST:event_check_in_date_jComboBoxActionPerformed

    private void check_out_date_jComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_check_out_date_jComboBoxActionPerformed
        // TODO add your handling code here:
        if (check_out_date_jComboBox.getSelectedIndex() != 0) {
            if (check_in_date_jComboBox.getSelectedIndex() == 0) {
                JOptionPane.showMessageDialog(null, "Please Input Check-In-Date First!", "WARNING", JOptionPane.WARNING_MESSAGE);
                check_out_date_jComboBox.setSelectedIndex(0);
            } else {
                DateTimeFormatter df = DateTimeFormatter.ofPattern("yyyy-MM-dd");
                LocalDate check_out_date = LocalDate.parse(check_out_date_jComboBox.getSelectedItem().toString(), df);
                LocalDate check_in_date = LocalDate.parse(check_in_date_jComboBox.getSelectedItem().toString(), df);
                Period next = Period.between(check_in_date, check_out_date);
                int days = next.getDays();
                if (days <= 0) {
                    check_out_date_warning_label.setVisible(true);
                    JOptionPane.showMessageDialog(null, "Check Out date should behind Check In date !", "WARNING", JOptionPane.WARNING_MESSAGE);
                    check_out_date_jComboBox.setSelectedIndex(0);
                } else if (days > 4) {
                    check_out_date_warning_label.setVisible(true);
                    JOptionPane.showMessageDialog(null, "Can not live more than 4 days !", "WARNING", JOptionPane.WARNING_MESSAGE);
                    check_out_date_jComboBox.setSelectedIndex(0);
                } else {
                    check_out_date_warning_label.setVisible(false);
                    Nights_jComboBox.setSelectedItem(String.valueOf(days));
                }
            }
        } else {
            check_out_date_warning_label.setVisible(true);
        }
    }//GEN-LAST:event_check_out_date_jComboBoxActionPerformed

