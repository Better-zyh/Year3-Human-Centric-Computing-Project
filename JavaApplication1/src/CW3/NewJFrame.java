    private void Name_textActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Name_textActionPerformed
        // TODO add your handling code here:
        Pattern p = Pattern.compile(".*\\d+.*");
        Matcher m = p.matcher(Name_text.getText());
        if (Name_text.getText().equals("")) {
            name_warning_label.setVisible(true);
        } else if (m.matches()) {
            name_warning_label.setVisible(true);
        } else {
            name_warning_label.setVisible(false);
        }
    }//GEN-LAST:event_Name_textActionPerformed

    private void Nights_jComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Nights_jComboBoxActionPerformed
        // TODO add your handling code here:z
        if (Nights_jComboBox.getSelectedIndex() != 0) {
            if (check_in_date_jComboBox.getSelectedIndex() == 0) {
                JOptionPane.showMessageDialog(null, "Please Select Check-In-Date First!", "WARNING", JOptionPane.WARNING_MESSAGE);
                Nights_jComboBox.setSelectedIndex(0);
                nights_warning_label.setVisible(true);
            } else {
                int nights = Integer.parseInt(Nights_jComboBox.getSelectedItem().toString());
                DateTimeFormatter df = DateTimeFormatter.ofPattern("yyyy-MM-dd");
                LocalDate check_in_date = LocalDate.parse(check_in_date_jComboBox.getSelectedItem().toString(), df);
                LocalDate temp = check_in_date.plusDays(nights);
                check_out_date_jComboBox.setSelectedItem(temp.toString());
            }
            nights_warning_label.setVisible(false);
        } else {
            nights_warning_label.setVisible(true);
        }
    }//GEN-LAST:event_Nights_jComboBoxActionPerformed

